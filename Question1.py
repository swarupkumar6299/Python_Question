#Average Calculator: Write a program that prompts the user for three numbers and calculates their average.
num1 = int(input("Enter first no:- "))
num2 = int(input("Enter second no:- "))
num3 = int(input("Enter Third no:- "))
Average = (num1+num2+num3)/100
print(f"The average number of{num1},{num2},{num3} is :- {Average}")
